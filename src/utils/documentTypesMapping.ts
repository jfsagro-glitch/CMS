/**
 * Получить название листа Excel для типа имущества
 */
export function getDocumentSheetName(propertyType: string): string {
  // Прямое совпадение
  if (DOCUMENT_TYPE_MAPPING[propertyType]) {
    return DOCUMENT_TYPE_MAPPING[propertyType];
  }
  
  // Поиск по частичному совпадению
  const lowerType = propertyType.toLowerCase();
  for (const [key, sheetName] of Object.entries(DOCUMENT_TYPE_MAPPING)) {
    if (lowerType.includes(key.toLowerCase()) || key.toLowerCase().includes(lowerType)) {
      return sheetName;
    }
  }
  
  // По умолчанию - общий список
  return 'ТС и СМ'; // Наиболее распространенный тип
}

