export const loadEGRNDemoData = (): void => {
  try {
    const existing = localStorage.getItem('egrnRequests');
    if (!existing || JSON.parse(existing).length === 0) {
      const demoData = generateEGRNDemoData();
      localStorage.setItem('egrnRequests', JSON.stringify(demoData));
      console.log('✅ Демо-данные ЕГРН загружены');
    } else {
      // Проверяем, есть ли выписки ЕГРН, если нет - добавляем их
      const existingData = JSON.parse(existing) as EGRNRequest[];
      const hasExtracts = existingData.some(item => item.serviceType === 'extract');
      if (!hasExtracts) {
        const allDemoData = generateEGRNDemoData();
        const extracts = allDemoData.filter(item => item.serviceType === 'extract');
        const updatedData = [...existingData, ...extracts];
        localStorage.setItem('egrnRequests', JSON.stringify(updatedData));
        console.log('✅ Демо-данные выписок ЕГРН добавлены');
      }
    }
  } catch (error) {
    console.error('Ошибка загрузки демо-данных ЕГРН:', error);
  }
};

